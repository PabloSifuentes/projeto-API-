package com.senai.Cadastro.dto;

public class RespostaDto {

    private int id;
    private String usuario;

    public RespostaDto(){
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
}
